      <h1>Definición y objetivos</h1>
      <h3>DEFINICIÓN:</h3>
      <p>Estadía es un programa institucional que proporciona a los alumnos la práctica en un ambiente laboral real para aplicar los conocimientos adquiridos, aportando soluciones pertinentes y factibles.</p>
      <h3>OBJETIVO:</h3>
      <p>Que los alumnos obtengan experiencia en el sector productivo o de investigación, mediante un procedimiento ordenado que fortalezca, complemente y oriente su formación académica basada en competencias y valores; poniendo en práctica sus conocimientos participando en la solución de problemas para contribuir al desarrollo de la Región, el Estado y el País.</p>
      <p><a name="requisitos"></a></p>
      <h1>Requisitos</h1>
      <ol>
      <li>Copia de tu Constancia de Acreditación de Servicio Social o la copia del reporte final en caso de haber realizado Servicio Social interno o copia de tu carta de Terminación  de servicio social en caso de haberlo realizado externo.</li>
      <li>Dos fotografías ovalo (para certificado) blanco y negro, de frente con retoque y  vestimenta formal, recientes, no instantaneas. Colocar su nombre y carrera al reverso de las fotografías.</li>
      <li>Copia de tu hoja de seguro facultativo IMSS, ISSSTE o PEMEX únicamente.</li>
      <li>Copia de tu recibo de pago de reinscripción del cuatrimestre en que vayas a realizar estadía.</li>
      <li>Curriculum Vitae impreso en un máximo de tres hojas.</li>
      <li>Constancia de no adeudo de materias expedida por servicios escolares.</li>
      <li>Dar de alta tu proceso de estadía en tu sesión de alumnos en el icono de estadía y servicio social.</li>
      </ol>
